    private void NakitÖdeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NakitÖdeBtnActionPerformed
        // TODO add your handling code here:
        int fiyat = count;
        if (count == fiyat)
        {
            fiyat = count - (count / 10);

            ToplamHesap.setText("10% discount applied. New payment: " + fiyat);
        }
    }//GEN-LAST:event_NakitÖdeBtnActionPerformed

    private void İndirimBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_İndirimBtnActionPerformed
        // TODO add your handling code here:
        if (İndirimKodu.getText().equalsIgnoreCase("Hayvanlarıkoruyun"))
        {
            ToplamHesap.setText("Discount Code Successful! New payment: " + (count - (count / 10)));
            count = count - (count / 10);

            İndirimBtn.setEnabled(false);
        }
    }//GEN-LAST:event_İndirimBtnActionPerformed

