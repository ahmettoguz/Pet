    private void HesapBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HesapBtnActionPerformed
        // TODO add your handling code here:
        count = 0;

        if (KediMamaCbx.isSelected())
        {
            count += 140;
        }
        if (Tasmacbx.isSelected())
        {
            count += 100;
        }
        if (KöpekMamaCbx.isSelected())
        {
            count += 150;
        }
        if (KöpekOyuncakCbx.isSelected())
        {
            count += 50;
        }
        if (KafesCbx.isSelected())
        {
            count += 200;
        }
        if (KediOyuncakCbx.isSelected())
        {
            count += 350;
        }

        if (!KediMamaCbx.isSelected() && !Tasmacbx.isSelected() && !KöpekMamaCbx.isSelected() && !KöpekOyuncakCbx.isSelected()
                && !KafesCbx.isSelected() && !KediOyuncakCbx.isSelected())
        {
            ToplamHesap.setText("Please select product!");
        } else
        {
            //ToplamHesap.setText("ALIŞVERİŞ TAMAMLANDI"); yeni satır gibi yazmadım
            ToplamHesap.setText("Total payment: " + count + "TL");
        }


    }//GEN-LAST:event_HesapBtnActionPerformed

